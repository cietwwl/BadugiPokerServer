package com.badugi.game.logic.model.vo.flash.operators;

public class SpreadChips {

	private Integer ms;
	private Integer mc;
	
	
	public SpreadChips(Integer ms, Integer mc) {
		super();
		this.ms = ms;
		this.mc = mc;
	}
	
	public SpreadChips() {
		super();
	}

	public Integer getMs() {
		return ms;
	}
	public void setMs(Integer ms) {
		this.ms = ms;
	}
	public Integer getMc() {
		return mc;
	}
	public void setMc(Integer mc) {
		this.mc = mc;
	}
	
	
	
}
